# Multiple Conditions with Nesting
# Write a program that checks if a number is divisible by 2. If true, check if it's also divisible by 3.
# Print "Divisible by both 2 and 3"
# If only divisible by 2, print "Only divisible by 2"?
num=6
if num%2==0:
    if num%3==0:
        print("Divisible by both 2 and 3")
    else:
        print("Only divisible by 2")   

    
